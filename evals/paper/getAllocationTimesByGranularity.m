function [allocationTimes] = getAllocationTimesByGranularity(appname, appdatapath, params)
    
    BASE_PATH = '../../malloc/evals/granularity/32';
    NUM_DATA_POINTS = 100;
    NUMAPPS = 32;

    allocationTimes = zeros(NUMAPPS * NUM_DATA_POINTS, length(params));

    for i = 1:length(params)
        for j = 1:NUM_DATA_POINTS
            data_alloctime = readtable(sprintf( ...
                '%s/%s/stats_g%d_n%d_%s/%d/alloctime.csv', ...
                BASE_PATH, ...
                appdatapath, ...
                params(i), ...
                NUMAPPS, ...
                appname, ...
                j - 1 ...
            ));
            numsamples = size(data_alloctime, 1);
            allocationTimes((j-1) * 32 + 1 : min((j*32), (j-1) * 32 + numsamples), i) = data_alloctime{ : , 1} * 1E3;
            allocationTimes(allocationTimes == 0) = NaN;
        end
    end
end